    void tampil(){
        DefaultTableModel tabelscript=new DefaultTableModel();
        
        tabelscript.addColumn("Kode MataPelajaran");
        tabelscript.addColumn("Nama Matapelajaran");
        tabelscript.addColumn("Guru");
        

        
        
        try {
            koneksi();
            String sql="select * from view_mp";
            stmnt=kon.createStatement();
            rs=stmnt.executeQuery(sql);
            while(rs.next()){
                tabelscript.addRow(new Object[]{rs.getString(1), rs.getString(2),rs.getString(3)});
            }
            jTable1.setModel(tabelscript);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error "+ex.getMessage());
        }
    }
    
